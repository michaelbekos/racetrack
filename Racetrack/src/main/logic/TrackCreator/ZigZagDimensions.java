package src.main.logic.TrackCreator;

public class ZigZagDimensions
{
	public int minX;
	public int maxX;
	public int minY;
	public int maxY;
	ZigZagDimensions(int minX, int maxX, int minY, int maxY)
	{
		this.minX = minX;
		this.maxX = maxX;
		this.minY = minY;
		this.maxY = maxY;
	}
}
